#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*last;

	last = 0;
	if (!*s)
		return (0);
	while (1)
	{
		if (*s == c)
			last = (char *)s;
		if (*s++ == '\0')
			return (last);
	}
}
